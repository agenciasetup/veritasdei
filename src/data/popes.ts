function popeEmail(name: string): string {
  return 'papa' + name
    .normalize('NFD').replace(/[\u0300-\u036f]/g, '')
    .replace(/\s+/g, '').replace(/[^a-zA-Z]/g, '').toLowerCase()
    + '@veritasdei.com.br'
}

